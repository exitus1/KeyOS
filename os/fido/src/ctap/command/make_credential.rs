#[derive(Debug)]
pub struct Options {
    /// Specifies whether this credential is to be discoverable or not.
    pub rk: Option<bool>,

    /// user presence: Instructs the authenticator to require user consent to complete the operation.
    /// Platforms MAY send the "up" option key to CTAP2.1 authenticators, and its value MUST be true if
    /// present. The value false will cause a CTAP2_ERR_INVALID_OPTION response regardless of authenticator
    /// version.
    pub up: Option<bool>,

    /// user verification: If true, instructs the authenticator to require a user-verifying gesture in order
    /// to complete the request. Examples of such gestures are fingerprint scan or a PIN.
    /// NOTE: Use of this "uv" option key is deprecated in CTAP2.1. Instead, platforms SHOULD create a
    /// pinUvAuthParam by obtaining pinUvAuthToken via getPinUvAuthTokenUsingUvWithPermissions or
    /// getPinUvAuthTokenUsingPinWithPermissions, as appropriate.
    /// Platforms MUST NOT include the "uv" option key if the authenticator does not support built-in user
    /// verification.
    /// Platforms MUST NOT include both the "uv" option key and the pinUvAuthParam parameter in the same
    /// request.
    pub uv: Option<bool>,
}
impl Default for Options {
    fn default() -> Self { Self { rk: None, up: None, uv: None } }
}
impl<'b, C> minicbor::Decode<'b, C> for Options {
    fn decode(d: &mut minicbor::Decoder<'b>, _ctx: &mut C) -> Result<Self, minicbor::decode::Error> {
        let mut len = d.map()?;
        let mut opts = Options::default();
        while len.map(|l| l > 0).unwrap_or(false) {
            len = len.map(|l| l - 1);
            match d.str()? {
                "rk" => opts.rk = Some(d.bool()?),
                "up" => opts.up = Some(d.bool()?),
                "uv" => opts.uv = Some(d.bool()?),
                _ => d.skip()?,
            }
        }
        Ok(opts)
    }
}

#[derive(Debug, minicbor::Decode)]
#[cbor(map)]
pub struct MakeCredentialRequest {
    /// Hash of the ClientData contextual binding specified by host. See [WebAuthn].
    #[n(0x01)]
    pub client_data_hash: ClientDataHash,

    /// This PublicKeyCredentialRpEntity data structure describes a Relying Party with which the new public
    /// key credential will be associated. It contains the relying party identifier (rp.id of type text
    /// string, (optionally) a human-friendly RP name of type text string. The RP name is to be used by the
    /// authenticator when displaying the credential to the user for selection and usage authorization. The
    /// RP name and URL are OPTIONAL so that the RP can be more privacy friendly if it chooses to. For
    /// example, for authenticators with a display, RP may not want to display name for single-factor
    /// scenarios.
    /// NOTE: [WebAuthn-2] has removed the optional icon member. Authenticators MUST NOT error if the icon
    /// member is present, they MAY not store this value.
    #[n(0x02)]
    pub rp: PublicKeyCredentialRpEntity,

    /// This PublicKeyCredentialUserEntity data structure describes the user account to which the new public
    /// key credential will be associated at the RP.
    /// It contains an RP-specific user account identifier of type byte string, (optionally) a user name of
    /// type text string, (optionally) a user display name of type text string, and (optionally) a URL of
    /// type text string, referencing a user icon image (of a user avatar, for example). Note that while an
    /// empty account identifier is valid, it has known interoperability hurdles in practice and platforms
    /// are RECOMMENDED to avoid sending them.
    /// The authenticator associates the created public key credential with the account identifier, and MAY
    /// also associate any or all of the user name, and user display name. The user name and display name are
    /// OPTIONAL for privacy reasons for single-factor scenarios where only user presence is required. For
    /// example, in certain closed physical environments like factory floors, user presence only
    /// authenticators can satisfy RP’s productivity and security needs. In these environments, omitting user
    /// name and display name makes the credential more privacy friendly. Although this information is not
    /// available without user verification, devices which support user verification but do not have it
    /// configured, can be tricked into releasing this information by configuring the user verification.
    /// NOTE: [WebAuthn-2] has removed the optional icon member. Authenticators MUST NOT error if the icon
    /// member is present, they MAY not store this value.
    #[n(0x03)]
    pub user: PublicKeyCredentialUserEntity,

    #[n(0x04)]
    /// List of supported algorithms for credential generation, as specified in [WebAuthn]. The array is
    /// ordered from most preferred to least preferred and MUST NOT include duplicate entries.
    /// PublicKeyCredentialParameters' algorithm identifiers are values that SHOULD be registered in the IANA
    /// COSE Algorithms registry [IANA-COSE-ALGS-REG].
    pub pub_key_cred_params: Vec<PublicKeyCredentialParameters>,

    #[n(0x05)]
    /// PublicKeyCredentialDescriptor structures, as specified in [WebAuthn]. The authenticator returns an
    /// error if the authenticator already contains one of the credentials enumerated in this array. This
    /// allows RPs to limit the creation of multiple credentials for the same account on a single
    /// authenticator. If this parameter is present, it MUST NOT be empty.
    pub exclude_list: Option<Vec<PublicKeyCredentialDescriptor>>,

    #[n(0x06)]
    /// Parameters to influence authenticator operation, as specified in [WebAuthn]. These parameters might
    /// be authenticator specific.
    pub extensions: Option<Vec<Extension>>,

    #[n(0x07)]
    /// Parameters to influence authenticator operation.
    pub options: Option<Options>,

    #[n(0x08)]
    /// Result of calling authenticate(pinUvAuthToken, clientDataHash)
    pub pin_uv_auth_param: Option<Vec<u8>>,

    #[n(0x09)]
    /// PIN/UV protocol version chosen by the platform
    pub pin_uv_auth_protocol: Option<usize>,

    #[n(0x0a)]
    /// enterprise attestation feature is enterprise attestation capable and signals its support via the ep
    /// Option ID in the authenticatorGetInfo command response.
    /// If the enterpriseAttestation parameter is absent, attestation’s privacy characteristics are
    /// unaffected, regardless of whether the enterprise attestation feature is presently enabled.
    /// If present with a valid value, the usual privacy concerns around attestation batching may not apply
    /// to the results of this operation and the platform is requesting an enterprise attestation that
    /// includes uniquely identifying information.
    pub enterprise_attestation: Option<usize>,

    #[n(0x0b)]
    /// A prioritized list of attestation statement format identifiers that the client and/or RP prefers.
    /// Authenticators that support multiple formats may use this list to select a format compatible with the
    /// caller. Clients may request omission of attestation by including a single element with the string
    /// value "none".
    pub attestation_formats_preference: Option<Vec<String>>,
}
impl MakeCredentialRequest {
    pub fn from_cbor(data: &[u8]) -> Result<MakeCredentialRequest, Error> {
        Ok(minicbor::decode::<MakeCredentialRequest>(data)?)
    }
}

