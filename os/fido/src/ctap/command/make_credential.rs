impl<C> minicbor::Encode<C> for AttestationStatement {
    fn encode<W: minicbor::encode::Write>(
        &self,
        e: &mut minicbor::Encoder<W>,
        _ctx: &mut C,
    ) -> Result<(), minicbor::encode::Error<W::Error>> {
        e.map(3)?
            .str("alg")?
            .i32(self.alg as i32)?
            .str("sig")?
            .bytes(&self.sig)?
            .str("x5c")?
            .array(self.x5c.len() as u64)?;
        for x5c in self.x5c.iter() {
            e.bytes(x5c)?;
        }
        e.ok()
    }
}

#[derive(Debug, minicbor::Encode)]
#[cbor(map)]
pub struct MakeCredentialResponse {
    #[n(0x01)]
    /// The attestation statement format identifier.
    pub fmt: String,

    #[n(0x02)]
    /// The authenticator data object.
    pub auth_data: AuthData,

    #[n(0x03)]
    /// The attestation statement, as specified in [WebAuthn], if one is provided.
    att_stmt: Option<AttestationStatement>,

    #[n(0x04)]
    /// Indicates whether an enterprise attestation was returned for this credential. If epAtt is absent or
    /// present and set to false, then an enterprise attestation was not returned. If epAtt is present and
    /// set to true, then an enterprise attestation was returned.
    pub ep_att: Option<bool>,

    #[n(0x05)]
    /// Contains the largeBlobKey for the credential, if requested with the largeBlobKey extension.
    pub large_blob_key: Option<Vec<u8>>,

    #[n(0x06)]
    /// dentifiers, to unsigned outputs of extensions, if any. Authenticators SHOULD omit this field if no
    /// processed extensions define unsigned outputs. Clients MUST treat an empty map the same as an omitted
    /// field.
    pub unsigned_extension_outputs: Option<ExtensionOutputs>,
}
impl MakeCredentialResponse {
    pub fn new(rp_id: &str) -> MakeCredentialResponse {
        MakeCredentialResponse {
            fmt: "none".to_string(),
            auth_data: AuthData::new(rp_id),
            att_stmt: None,
            ep_att: None,
            large_blob_key: None,
            unsigned_extension_outputs: None,
        }
    }

    pub fn add_unsigned_extention_output_data(&mut self, ext_output: ExtensionOutput) {
        if let Some(unsigned_extension_outputs) = &mut self.unsigned_extension_outputs {
            unsigned_extension_outputs.0.push(ext_output);
        } else {
            let mut unsigned_extension_outputs = ExtensionOutputs::new();
            unsigned_extension_outputs.0.push(ext_output);
            self.unsigned_extension_outputs = Some(unsigned_extension_outputs);
        }
        self.auth_data.flags.ed = true;
    }

    pub fn attest(
        &mut self,
        client_data_hash: &[u8],
        public_key: &[u8],
        aaguid: [u8; 16],
        attestation_certificate: &[u8],
        attestation_pubkey: &[u8],
    ) -> Result<(), Error> {
        self.fmt = "packed".to_string();
        self.att_stmt =
            Some(AttestationStatement::prime(client_data_hash, public_key, attestation_certificate)?);
        self.auth_data.add_attest_credential_data(AttestedCredentialData::prime(
            aaguid,
            public_key,
            attestation_pubkey,
        ));
        Ok(())
    }

    pub fn to_vec_cbor(&self) -> Vec<u8> { minicbor::to_vec(self).unwrap() }
}

